package javapackage;

public class Different_Math_Methods_Program {
	
	public static void main (String[] args)
	{
		System.out.println(Math.addExact(100, 200));
		System.out.println(Math.subtractExact(4, 1));
		System.out.println(Math.multiplyExact(44, 31));
		System.out.println(Math.max(600, 8975));
		System.out.println(Math.max(3.542, 8.441));
		System.out.println(Math.min(10, 30));
		System.out.println(Math.min(0.22, 1.33));
		System.out.println(Math.sqrt(89));
	}

}
