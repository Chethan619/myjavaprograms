package javapackage;

public class The_Globalvariable_Value {
		
		static int n1=10;
		static double n2=11.2;
		static char c='o';
		int n3=4;
		double n4=3.6;
		String d="done";
		
		public static void main (String[] args)
		{
			System.out.println(n1);
			System.out.println(n2);
			System.out.println(c);
			The_Globalvariable_Value u1=new The_Globalvariable_Value();
			System.out.println(u1.n3);
			System.out.println(u1.n4);
			System.out.println(u1.d);
			
		}

			
		}

